#include "dict.h"

void	dict_free(t_dict *d)
{
	int	i;

	if (!d)
		return ;
	i = 0;
	while (d->e && i < d->size)
	{
		free(d->e[i].key);
		free(d->e[i].val);
		i++;
	}
	free(d->e);
	free(d);
}
